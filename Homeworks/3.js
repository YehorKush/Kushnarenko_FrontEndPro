// Створити скрипт для додавання, віднімання, множення та поділу двох
// чисел та виведення результатів.
// Користувач вводить два числа через два промпти, і повідомлення
// виводиться алертом з таким результатом:
//Користувач ввів 2 і 2:

// 2+2=4
// 2-2=0
// 2*2=4
// 2/2=1

let a = +prompt("Введіть перше число");
let b = +prompt("Введіть друге число");

alert(
  `${a}+${b}=${a + b}\n
${a}-${b}=${a - b}\n
${a}*${b}=${a * b}\n
${a}/${b}=${a / b}\n`
);
